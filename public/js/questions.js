function createSecondBlock() {
    let myBlock = document.getElementById("second-block");

    let displaySetting = myBlock.style.display;

    if (displaySetting == "block") {
        myBlock.style.display = "none";
    } else {
        myBlock.style.display = "block";
    }
}

